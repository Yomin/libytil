#ifndef YTIL_DEF_COLOR_H_INCLUDED
#define YTIL_DEF_COLOR_H_INCLUDED


#define COLOR_OFF               "\x1B[0m"       ///< ANSI SGR color off
#define COLOR_BLACK             "\x1B[30m"      ///< ANSI SGR color black
#define COLOR_RED               "\x1B[31m"      ///< ANSI SGR color red
#define COLOR_GREEN             "\x1B[32m"      ///< ANSI SGR color green
#define COLOR_YELLOW            "\x1B[33m"      ///< ANSI SGR color yellow
#define COLOR_BLUE              "\x1B[34m"      ///< ANSI SGR color blue
#define COLOR_MAGENTA           "\x1B[35m"      ///< ANSI SGR color magenta
#define COLOR_CYAN              "\x1B[36m"      ///< ANSI SGR color cyan
#define COLOR_WHITE             "\x1B[37m"      ///< ANSI SGR color white
#define COLOR_BRIGHT_BLACK      "\x1B[1;30m"    ///< ANSI SGR color bright black
#define COLOR_BRIGHT_RED        "\x1B[1;31m"    ///< ANSI SGR color bright red
#define COLOR_BRIGHT_GREEN      "\x1B[1;32m"    ///< ANSI SGR color bright green
#define COLOR_BRIGHT_YELLOW     "\x1B[1;33m"    ///< ANSI SGR color bright yellow
#define COLOR_BRIGHT_BLUE       "\x1B[1;34m"    ///< ANSI SGR color bright blue
#define COLOR_BRIGHT_MAGENTA    "\x1B[1;35m"    ///< ANSI SGR color bright magenta
#define COLOR_BRIGHT_CYAN       "\x1B[1;36m"    ///< ANSI SGR color bright cyan
#define COLOR_BRIGHT_WHITE      "\x1B[1;37m"    ///< ANSI SGR color bright white


#endif // ifndef YTIL_DEF_COLOR_H_INCLUDED
